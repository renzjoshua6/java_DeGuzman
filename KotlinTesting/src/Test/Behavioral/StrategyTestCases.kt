package Strategy

object StrategyTestCases {
    fun correctSum(num1: Int, num2: Int): Int {
        return num1 + num2
    }

    fun correctProduct(num1: Int, num2: Int): Int {
        return num1 * num2
    }

    fun correctDifference(num1: Int, num2: Int): Int {
        return num1 - num2
    }
}