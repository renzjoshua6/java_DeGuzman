package Strategy;

public class StrategyTestCases {

    public static int correctSum(int num1, int num2) {
        int sum = num1 + num2;
        return sum;
    }

    public static int correctProduct(int num1, int num2){
        int product = num1*num2;
        return product;

    }

    public static int correctDifference(int num1, int num2){
        int difference = num1-num2;
        return difference;
    }
}
